    <h2>日、韓文字之爭</h2>
    <p>眾所周知，BA是一款由韓國NEXON Games開發的遊戲。而又眾所周知的是，日服因為比國際服先開，所以日服比國際服進度要快。還眾所周知的是，日方運營Yostar會對文字進行潤色。</p>
    <p>以上事實體現出三點情況：一，遊戲的原始語言是韓文；二，日服內容會成為玩家重要的一手資源，甚至會成為玩家認可的“唯一官方版本”；三，韓、日文文字並不完全一致。</p>
    <p>看上去沒什麼問題，對吧？</p>
    <p>大錯特錯。如果你關注一些做BA劇情解析的UP主，那麼底下評論區裡提到日韓文文字之間異同的評論應該你也見多了。照理來說，因為日服在前，因此國際服理應最好對照日語來翻——你大概已經猜出來了：沒錯！除了繁中外，目前英、泰文其實都是按韓文來翻譯的！</p>
    <p>這種源語言的差異會導致一些很抽象的問題。在一開始的時候，大家都是參照的日服文字，結果就導致了有很多對不上的；後來，有懂韓文的老哥表示，這其中很多所謂的“翻譯問題”純粹是因為對的是日文，從韓文的角度看並沒有太大問題。</p>
    <p>由於日、韓文之間的明顯差異，有一些玩家也認為國服簡中翻譯是基於韓文文字翻譯的，這點我無法確認。</p>
    <p>造成日、韓文文字差異的原因很簡單：語言與文化差異。從結果看，BA日服在翻譯時採用了“異化翻譯”，即將文字改為更符合當地語言習慣和認知的形式，並且還對譯文進行了修改與潤色（並可能同時部分拯救了V1C3中星野的形象）。雖說也是改，但至少不是像英文區那樣離經叛道和瞎雞兒亂改，不過就算這樣也足以造成兩個文字之間的差異。</p>
    
