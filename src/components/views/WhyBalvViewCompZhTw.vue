    <h2>逆天的本地化文本</h2>
    <p>相較於上文中提到的英文區，作為母語為中文的我們簡直可謂為活在天堂。至少在翻譯上，劇情上我們有威威字幕君等團隊和個人，二創也有無數烤肉組與單幹的散戶，而且質量都很不錯；應當說，BA有一個這樣的社群是我們中文區玩家的福氣。根據我觀察的來看，英文區這邊雖確實也有幾位扛把子的，但似乎都是單幹，少見有團隊氣候。</p>
    <p>國服簡中與國際服繁中的情況這裡就不多介紹了，總結來說就是一個是和諧，一個是翻譯“還算可以”的地步。據說繁中還是經過幾次大概並切到了日語為原文翻譯的，只能說很幸運了。</p>
    <p>英文區其實一直以來都被NX的本地化搞得很不爽，因為除了明顯的翻譯錯誤外，英文區翻譯還加入了些許Reddit俚語爛梗，甚至不明所以地改掉一大段句子（尤見於部分大廳語音，特別是對老師有明顯表白、性暗示的）或加一些完全子虛烏有的東西。自2024年5月國際服百鬼夜行主線實裝以來，部分日、韓玩家就一直與英文區玩家聯手，不僅將其中的翻譯一一指出並做成一圖流等形式方便傳播，還號召廣大玩家去英文區官號底下留言並衝塔Nexon的客服中心，最終讓許多翻譯問題得到了解決與修正。</p>
