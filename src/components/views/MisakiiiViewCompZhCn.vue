  <p>哇，真没想到你居然真的会好奇到愿意点开那个链接。那既然你已经在这了，也许你可以坐和放宽一下，听我瞎鸡儿扯一点东西，然后再继续工作吧？当然了，你也可以随时返回到上一页继续。</p><br/>
  <p style="font-size: 1.1em"><i>致我挚爱的少女，阿里乌斯小队的<b>戒野美咲</b></i></p>
  <p>至今我都可以清晰记起第一次遇见美咲的情景。那会还是《伊甸条约》尚未签订的时候，也是整个小队首次出场之时。我对她的第一印象除了“性情忧郁”以外便无太多，因为这样的角色确实不算少见——但这倒也不是说我就对她之后试图跳桥自杀一事有所心理准备。</p>
  <p>颇为可惜的是，当时的我其实并未看出美咲的独特魅力，也并未意识到我与她在个人经历上的内在联系（尽管原因大相径庭）。毕竟，BA这游戏并不是以黑暗苦难的故事为主，而且像优香、日奈、未花这样的高人气角色也确实存在，于是大家自然就更没有理由去喜欢那些人气不高的角色（例如铃美），更别提像美咲这种还阴阴郁郁的角色了。</p>
  <p>直到，我意外歪出了她，并且之后开始清Momotalk的时候。</p>
  <p>在此，我觉得有必要先提一下我评价人物的一个标准，即“故事第一，立绘其次”，好看不好看确实不是我所特别考虑的因素。举例来说，我就不是看的FOX小队十分过眼，虽然确实好看，但她们在兔子小队后半篇干的事情也确实很难让人喜欢（尽管现实证明一切皆有可能）。美咲也是同理，她的故事可谓是真的触到了我心中的那根弦。</p>
  <p>美咲，本质上，就是一个将虚无主义作为一切的终极答案和唯一信仰的少女，一个我们在其他阿里乌斯学生（包括小梓）身上同样见到过的情况。但，就如Reddit用户<a target="_blank" href="https://www.reddit.com/user/Genprey/">Genprey</a>在其“学生档案”系列帖子中说的那样，美咲大概是小队里情况最紧急、最需要干预的一位。毕竟在主线里，美咲可不只是尝试自杀过一次（就是桥上那一次），而是在小时候就有多次尝试未遂，甚至连纱织都没法完全劝住她不那么干——单就这点其实就足以我们起同情心，但要真正让我成为“美咲单推人【我其实不是很喜欢用‘单推人’这个词，更偏向于用‘喜欢’这种偏中性的】”这种程度，则要深入到<RouterLink to="/scenario/1004105">她的第三个羁绊故事</RouterLink>来一探究竟，因为那也是我与她的联系真正建立之时。</p>
  <p>D.U.下雨的一天晚上，老师在街上见到了一只全身湿透、发着高烧的美咲。美咲承认自己已经有几天没吃没睡了，甚至一说完就直接倒在了地上。作为一名负责任的大人，老师把她带离了大街，转而在附近的一家酒店租了间房，暂时安置一下。老师告诉美咲换好衣服后就好好休息，随后便出门去买药了。</p>
  <p>酒店的房间里，美咲，再一次孤身一人。她开始咒骂起世间一切来，埋怨自己的身体不过是一个“无用的、总挡着她道的容器”罢了，然后又说起去死之类的事情。这一些其实都很好理解，但之后的情节，也就是Genprey所说的“最为重大的人物成长之进程”，真正地深得我心。</p>
  <p>美咲自言自语道，“我……好<b><i><u>孤独</u></i></b>。”这是美咲在故事中第一次出现的极为重要的心理转变，因为这暗示着她意识到了“老师不在我身边了”这件事，表现出她的老师的好感与依赖之情。同时，这也是美咲第一次在故事中放松了自己的戒备之心，反而抱怨起老师不在这件事起来。</p>
  <p>当老师回来时，美咲正坐在窗台边上，一个任何有自杀倾向者都会做的事情，但与其他人不同，就这一场景而言，我个人认为可以理解为她正在一点点接受世界之残酷和自己的弱点，以及更可能是在等着、期待着老师会回来。从她的回忆大厅语音来看，美咲显然平静了许多，对老师会一直在她身旁一事感到无限慰藉。</p>
  <p>美咲对世界的感知，即所谓“世界全为虚无，挣扎毫无意义”，也是一件我们许多人在一生中至少会经历一次的事情。比方说，学业压力就是这样一件能够轻易导致类似情况的东西；写作本文时，我本人就已经历了至少两次（目前高三则是第三次）。学业压力让人痛苦万分，让人精神上难以忍受，并业已成为导致青少年自杀的一大诱因，让人绝望而痛心。而作为一个曾经走出来过的人，我愿以我最大的一切去爱、去关心、去呵护美咲，且远远超过对其它人的程度——这种事情也就不奇怪了。</p>
  <p>但更令我伤感的则是我在查看美咲资料时，无意间读到的她自我介绍的语音文本：“（现在）我就像一只被抛弃的猎犬……（所以）狗绳给你，随你怎么使唤我吧。”称呼自己为“猎犬”（狗），而且还直白地用上了“狗绳”一词，这真的让我大吃一惊。我不觉得其他任何一个学生，甚至是任何一个小队成员，除了她，会这么称呼自己。读罢此行，我的心唯有震惊，真的痛到让人无法接受。</p>
  <br />
  <p>个把月后，国际服也迎来了0068歌剧活动，相当于情人节活动也复刻了一遍。顺着自己的好奇心和几位朋友的强烈推荐，我选了美咲的故事作为我的开场菜，而这个故事也确实没让我失望，给了我相当多的慰藉。</p>
  <p>以“检查生命线（utility lines，特指如水电等管道线路）”作为明面上的接口，最终，美咲红着脸、极不好意思地给老师送了自己亲手做的巧克力。虽然在情人节这种背景下，编剧这么做也是人之常理，但于我而言，这一点同样是美咲性格的一大进步，因为我们现在确切地知道了，美咲是记得老师的。</p>
  <p>当你心中有了你真正关心的人时，你一般就不会像以往一样鲁莽行事，因为你需要顾及那一个人的感受。在美咲这一例子中，美咲的自杀倾向已经没有以前那么明显了，而这不仅仅是因为老师曾帮助过她，更是因为她不希望让老师担心——同时，在这一过程中，美咲也找到了活下去的意义之一，即“见到老师”。</p>
  <p>对于其它少女，尤其是诺亚和一花，这种更进一步的亲密关系与情感很可能会让她们变成一些二创中此类所谓的「病娇」，十分危险。不过，对美咲、对整个小队来说，我个人是不觉得有什么问题的，甚至说我十分愿意给差口更多的抱抱、买更多的小熊，之类的。就像老师在终篇中说的那样，<i>“<b>没有</b>孩子，理应遭受痛苦。”</i></p>
  <br />
  <p>至于阿里乌斯的泳装活动（国际服也快了），只能说我确实还没找到时间去通读整个故事，所以大概会等到国际服实装后再读了。不过据说美咲在故事中终于笑了，所以我确实也挺期待这个活动的。但颇为遗撼的是，泳装美咲在此轮活动中并未实装，但有鉴于这也算是Nexon的经典“不一次实装完毕”策略了……等等觉永不言败！</p>
  <p>说起来，我也同时很高兴地能在推特上找到同样喜欢美咲的画师和单推人，而且几乎天天都有新推文。虽说二创数量确实还稳不到一天一张的水平，但见到有人真的愿意画差咲这事，我觉得我也应当要知足一点了。</p>
  <br />
  <p>最后，谢谢你到这里。了解美咲，我花了一些时间，而爱上美咲，则又让我花了更多一些时间。在此，我想引用Genprey佬文章中的一段内容：“如果你问我纱织在未来能否继续走下去，我的回答是能”——这一点，我对美咲也有着一样的愿景。漫漫长路艰险，努力不弃向前。</p>
  <p>顺便，如何你好奇的话，我确实还很喜欢其他两位，一个是歌原/咏叶（拉拉队），另一个则是小梓（普装/泳装）。实际上，我个人觉得梓与美咲也是有对比的空间的，因为两人初时信仰相同，如今最终却分别落到了所谓“积/消极虚无主义”的两个极端上。值得一提的是，梓也是我第一位同样感同身受颇深的角色，她在性格上的转变（更加阳光开朗）尤得我心。</p>
